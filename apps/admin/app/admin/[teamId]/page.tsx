export async function generateMetadata(
  { params, searchParams }: Props,
  parent: ResolvingMetadata
): Promise<Metadata> {
  const { teamId } = await params;
  const previousMetadata = (await parent).title;

  return {
    ...previousMetadata,
    alternates: {
      canonical: `/admin/${teamId}`,
    },
  };
}

